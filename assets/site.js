  const originalText = new WeakMap();
  const originalAlt = new WeakMap();
  const translateText = (text, lang) => {
    if (lang === "en") return text;
    return translations[lang]?.[text.trim()] || text;
  };

  const setLanguage = (lang) => {
    document.documentElement.lang = lang;
    document.querySelectorAll("body *").forEach((node) => {
      if (node.closest(".image-modal")) return;
      node.childNodes.forEach((child) => {
        if (child.nodeType !== Node.TEXT_NODE) return;
        const value = child.nodeValue;
        if (!value || !value.trim()) return;
        if (!originalText.has(child)) originalText.set(child, value);
        const source = originalText.get(child);
        const leading = source.match(/^\s*/)?.[0] || "";
        const trailing = source.match(/\s*$/)?.[0] || "";
        child.nodeValue = `${leading}${translateText(source, lang)}${trailing}`;
      });
      if (node instanceof HTMLImageElement && node.alt) {
        if (!originalAlt.has(node)) originalAlt.set(node, node.alt);
        node.alt = translateText(originalAlt.get(node), lang);
      }
    });
    localStorage.setItem("condeBuildsLang", lang);
    document.querySelectorAll("[data-language-toggle]").forEach((button) => {
      button.textContent = lang === "en" ? "Espanol" : "English";
      button.setAttribute("aria-label", lang === "en" ? "Switch site language to Spanish" : "Cambiar el sitio a ingles");
    });
  };

  const addLanguageToggle = () => {
    document.querySelectorAll(".nav-links").forEach((nav) => {
      if (nav.querySelector("[data-language-toggle]")) return;
      const button = document.createElement("button");
      button.type = "button";
      button.className = "language-toggle";
      button.setAttribute("data-language-toggle", "");
      button.addEventListener("click", () => {
        const current = localStorage.getItem("condeBuildsLang") || "en";
        setLanguage(current === "en" ? "es" : "en");
      });
      nav.appendChild(button);
    });
    setLanguage(localStorage.getItem("condeBuildsLang") || "en");
  };

  addLanguageToggle();

  const images = Array.from(document.querySelectorAll(".case-image img, .gallery img"));
  if (!images.length) return;

  const modal = document.createElement("div");
